// Helper functions to generate metadata for different page types
export function generateServiceMetadata(
  serviceKey: string,
  imageUrl: string = ""
): Metadata {
  const service =
    metadataConfig.services[serviceKey as keyof typeof metadataConfig.services];
  if (!service) {
    return {
      title: metadataConfig.default.title,
      description: metadataConfig.default.description,
    };
  }

  return {
    title: service.title,
    description: service.description,
    keywords: service.keywords,
    openGraph: {
      title: service.title,
      description: service.description,
      url: `https://www.urbandentalnj.com/services/${serviceKey}`,
      images: imageUrl
        ? [
            {
              url: imageUrl,
              width: 1200,
              height: 630,
              alt: service.title,
            },
          ]
        : undefined,
    },
    twitter: {
      card: "summary_large_image",
      title: service.title,
      description: service.description,
      images: imageUrl ? [imageUrl] : undefined,
    },
    alternates: {
      canonical: `https://www.urbandentalnj.com/services/${serviceKey}`,
    },
  };
}

